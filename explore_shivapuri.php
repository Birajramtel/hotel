                    the Budhanilkantha temple lies the famous Buddhist monastery ‘Naagi Gumba’, the only monastery 
                    for Nuns where about 200 of them practice Buddhism. Shivapuri also houses Baagdwar and Bishnudwar, 
                    the origin points of sacred rivers Bagmati and Bishnumati respectively. Every year on Baisakh 1, 
